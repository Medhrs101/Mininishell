#include "libft.h"

char	*ft_strdup(const char *s1)
{
	char	*str;
	int		i;

	if (s1 == NULL)
		return (NULL);
	i = 0;
	while (s1[i])
		i++;
	str = (char *)malloc((i + 1) * sizeof(char));
	if (!str)
		return (NULL);
	i = 0;
	while (s1[i])
	{
		str[i] = s1[i];
		i++;
	}
	str[i] = '\0';
	return (str);
}
